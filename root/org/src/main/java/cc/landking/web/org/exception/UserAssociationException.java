package cc.landking.web.org.exception;

public class UserAssociationException extends RuntimeException {

	public UserAssociationException() {
		super();
	}

	public UserAssociationException(String message, Throwable cause) {
		super(message, cause);
	}

	public UserAssociationException(String message) {
		super(message);
	}

	public UserAssociationException(Throwable cause) {
		super(cause);
	}

}
